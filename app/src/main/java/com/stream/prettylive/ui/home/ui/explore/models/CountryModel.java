package com.stream.prettylive.ui.home.ui.explore.models;

public class CountryModel {

    String image;
    String name;

    public CountryModel(String image, String name) {
        this.image = image;
        this.name = name;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
